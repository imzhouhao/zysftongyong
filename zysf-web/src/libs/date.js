/**
 * 获取前N天日期
 * @param num
 * @returns {Date}
 */
export function getEarlyDay(num) {
  //获取当前日期
  let date = new Date().getTime();
  //(一天24小时，一小时60分钟，一分钟60秒，一秒1000毫秒)* num天
  return  new Date(date - 24 * 60 * 60 * 1000 * num);//获取多少天前的时间
}


/**
 * 判断新闻资讯等是不是最新的，主要有两个参数，一个是资讯日期，一个是设定的离今天多久算新
 * @param infoDate 信息的日期
 * @param offset 日期偏移量
 */
export function isInfoNew(infoDate, offset){
  let infoDate1 = new Date(infoDate);
  let earlyDay = getEarlyDay(offset);
  return infoDate1.getTime() > earlyDay.getTime();
}